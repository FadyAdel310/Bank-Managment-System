    public static void loadAccountsData() {
        try {
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl","hr","hr");
            Statement stmt = con.createStatement();
            String command = "select * from "+Bank.accountsTable;
            ResultSet rs = stmt.executeQuery(command);
            while(rs.next()) {
                String accountNum = rs.getString("ACCOUNT_NUM");
                String accType = rs.getString("TYPE");
                float balance = Float.parseFloat(rs.getString("BALANCE"));
                String status = rs.getString("STATUS");
                String clientId = rs.getString("CLIENT_ID");
                
                String year = rs.getString("YEAR");
                String month = rs.getString("MONTH");
                String day = rs.getString("DAY");
                String dateString = year+"-"+month+"-"+day;
                LocalDate startDate = Bank.getDateFromString(dateString);
                Account ac = new Account(accountNum, accType, balance, status, startDate);
                Bank.getClientById(clientId).accounts.add(ac);
            }
            con.close();
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }
    
    public static void loadTransactoinsData() {
        try {
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl","hr","hr");
            Statement stmt = con.createStatement();
            String command = "select * from "+Bank.transactionsTable;
            ResultSet rs = stmt.executeQuery(command);
            while(rs.next()) {
                String id = rs.getString("TRANSACTION_ID");
                float amount = Float.parseFloat(rs.getString("AMOUNT"));
                String senderNum = rs.getString("SENDER_NUM");
                String receiverNum = rs.getString("RECEIVER_NUM");

                String year = rs.getString("YEAR");
                String month = rs.getString("MONTH");
                String day = rs.getString("DAY");
                String dateString = year+"-"+month+"-"+day;
                LocalDate date = Bank.getDateFromString(dateString);

                Transaction tr = new Transaction(id, amount, date, senderNum, receiverNum);
                Bank.transactions.add(tr);
                
            }
            con.close();
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }
    

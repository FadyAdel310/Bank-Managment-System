    public static void loadAdminsData() {
        try {
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl","hr","hr");
            Statement stmt = con.createStatement();
            String command = "select * from "+Bank.adminsTable;
            ResultSet rs = stmt.executeQuery(command);
            while(rs.next()) {
                String id = rs.getString("id");
                String firstName = rs.getString("FIRST_NAME");
                String lastName = rs.getString("LAST_NAME");
                String email = rs.getString("EMAIL");
                String password = rs.getString("PASSWORD");
                Admin ad = new Admin(id, firstName, lastName, email, password);
                Bank.admins.add(ad);
            }
            con.close();
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }

    public static void loadEmployeesData() {
        try {
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl","hr","hr");
            Statement stmt = con.createStatement();
            String command = "select * from "+Bank.employeesTable;
            ResultSet rs = stmt.executeQuery(command);
            while(rs.next()) {
                String position = rs.getString("POSITION");
                boolean accepted = Boolean.parseBoolean(rs.getString("ACCEPTED_FLAG"));
                
                String college = rs.getString("COLLEAGE");
                int year = Integer.parseInt(rs.getString("YEAR"));
                String grade = rs.getString("GRADE");
                Qualification qualify = new Qualification(college, year, grade);
                
                String id = rs.getString("id");
                String firstName = rs.getString("FIRST_NAME");
                String lastName = rs.getString("LAST_NAME");
                String email = rs.getString("EMAIL");
                String password = rs.getString("PASSWORD");
                
                Employee emp = new Employee(position, accepted, qualify, id, firstName, lastName, email, password);
                Bank.employees.add(emp);
            }
            con.close();
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }
    
